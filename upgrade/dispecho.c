#include <stdlib.h>
#include <stdio.h>
#include <unistd.h>
#include <string.h>
#include <sys/ioctl.h>
#include <sys/types.h>
#include <sys/stat.h>
#include <signal.h>
#include <fcntl.h>

int main(int argc, char **argv)
{
    int displayfd;

    printf("%s\n", argv[1]);
    
    displayfd = open("/dev/spi", O_WRONLY);
    if (displayfd >= 0)
    {
	write(displayfd,"\x1f\x24\x00\x00\x00\x00", 6);
	write(displayfd, argv[1], strlen(argv[1]));
    }

    return 0;
}
